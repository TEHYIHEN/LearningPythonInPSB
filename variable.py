    #1. variable naming rules(cannot break)
    #- can only contain alphabets ,_ and numbers
    #- must start with alphabets or _
    #- cannot be keywords
    # eg. valid names are:
    # a
    # _theskyisblue
    # num_people
    #
    # eg. invalid names are:
    # 4ever  -> cannot start with number
    # this+ that -> cannot contain +
    # class -> is a keyword

    #2.good variable names(guideline)
    # - must be meaningful
    # - use all lower case for variables and function names
    # - use _ to connect words
    # eg. good names
    # age
    # student_count
    # is_valid_number
    #
    # eg. bad names
    # a     #what does it mean?
    # s_count   #what s mean?
    # function_of_xxxxx     #too long variable name
    # _full_name    #don't start with _

