import math

# print(x) function which takes in a parameter x
#   "hello world" is the argument to print()
#   returns nothing (should not assign it to a variable)
some_valie = print("hello world")
print(some_valie)  #print result = None

# sqrt(x) function takes in a single parameter x
#   16 is the argument to sqrt()
#   returns 4 (which is the result that must be assigned)
result = math.sqrt(16)  # gives you 4 which is then assigned to result
print(result)